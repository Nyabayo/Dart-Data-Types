//number data type
//numeric value storage using in,double which are subtypes of num
//int stores whole numbers
//double stores decimal numbers
void main() {
  int num1 = 100;
  double num2 = 130.2;
  num num3 = 50;
  num num4 = 43.6;

  //sum
  num sum = num1 + num2 + num3 + num4;

  //printing
  print('$num1 \n $num2 \n $num3 \n $num4 \n $sum');
}
